// MATH - Função matemática
let num1 = 9.54343;
let num2 = Math.floor(num1); // floor arredonda o número para baixo
let num3 = Math.ceil(num1); // Arredonda para o próximo número de cima
let num4 = Math.round(num1); // arredonda para o número mais próximo, depende do valor do número
console.log(num2)
console.log(num3)
console.log(num4)

console.log(Math.max(1, 2, 4, 5, 20, 800, -84)) // Verifica o maior número
console.log(Math.min(1, 2, 4, 5, 20, 800, -84)) // Verifica o menor número
console.log(Math.random()) // Gera um número aleatório entre 0 e 1

const aleatorio = Math.round(Math.random() * (10 - 5) + 5); // Número aleatório entre 10 e 5
console.log(aleatorio)

